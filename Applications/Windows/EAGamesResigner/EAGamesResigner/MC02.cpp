DWORD CEAResigner::CalculateEAChecksum(BYTE* data, DWORD size) {
	DWORD rotnored = _rotr(data[0], 8) | _rotr(data[1], 16);
	DWORD nrotor = rotnored | _rotl(data[2], 8);
	DWORD last = ~(nrotor | data[3]);
	
	for (DWORD count = 4; count < size; count++)
	{
		DWORD rotnm = _rotl(last, 10) & MASK(22, 29);
		last <<= 8;
		last |= data[count];
		rotnm = bswap32(*(DWORD*)(EACRCTable + rotnm));
		last ^= rotnm;
	}

	return ~last;
}

BOOL CEAResigner::ResignFile(FILE *infile) {
	if (!infile)
		return FALSE;

	MC02_HEADER* HeaderPtr;

	fseek(infile, 0, SEEK_END);
	DWORD FileSize = ftell(infile);
	rewind(infile);

	BYTE* filebuffer = new BYTE[FileSize];
	fread(filebuffer, 1, FileSize, infile);
	
	HeaderPtr = (MC02_HEADER*)filebuffer;

	if (HeaderPtr->HeaderMagic != 0x3230434D) {
		free(filebuffer);
		return FALSE;
	}

	// Horray big endian!
	HeaderPtr->FirstFileChecksum = bswap32(this->CalculateEAChecksum((filebuffer + 0x1C), bswap32(HeaderPtr->FistFileSize)));
	HeaderPtr->SecondFileChecksum = bswap32(this->CalculateEAChecksum((filebuffer + 0x1C + bswap32(HeaderPtr->FistFileSize)), bswap32(HeaderPtr->SecondFileSize)));
	HeaderPtr->HeaderChecksum = bswap32(this->CalculateEAChecksum(filebuffer, 0x18));

	fseek(infile, 0, SEEK_SET);
	fwrite(filebuffer, 1, 0x1C, infile); // Only write 0x1C because thats all that was modified
	fflush(infile);

	free(filebuffer);
	return TRUE;
}