DWORD MASK(int mb, int me)
{
	//first make 001111111111111 part
	DWORD begin = 0xFFFFFFFF >> mb;
	//then make 000000000001111 part, which is used to flip the bits of the first one
	DWORD end = me < 31 ? (0xFFFFFFFF >> (me + 1)) : 0;
	//do the bitflip
	DWORD mask = begin ^ end;
	//and invert if backwards
	if (me < mb) 
		return ~mask;
	else
		return mask;
}

DWORD bswap32(DWORD x) {
	return  ((x << 24) & 0xff000000 ) |
			((x <<  8) & 0x00ff0000 ) |
			((x >>  8) & 0x0000ff00 ) |
			((x >> 24) & 0x000000ff );
}

